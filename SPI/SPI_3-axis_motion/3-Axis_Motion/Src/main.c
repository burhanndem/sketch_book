// led flash for Z-axis
void GYROFUNC_Z_FlashLED()
{
		
		if( fabs(data_xyz.z) > 0 && fabs(data_xyz.z) < 125)
		{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
			HAL_Delay(50);
		}
		if( fabs(data_xyz.z) > 125 && fabs(data_xyz.z) < 250)
		{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
			HAL_Delay(100);
		}
		if( fabs(data_xyz.z) > 250 && fabs(data_xyz.z) < 450)
		{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
			HAL_Delay(150);
		}
		if( fabs(data_xyz.z) > 450 && fabs(data_xyz.z) < 650)
		{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
			HAL_Delay(200);
		}
		if( fabs(data_xyz.z) > 650 && fabs(data_xyz.z) < 950)
		{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
			HAL_Delay(250);
		}
		if( fabs(data_xyz.z) > 950 && fabs(data_xyz.z) < 1100)
		{
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
		}
		
		
			
}

